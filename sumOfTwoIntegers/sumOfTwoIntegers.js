/**
* A method to add properties `a` and `b` but without using any mathamatical operators
* @param `number` - The first number to be added
* @param `number` - The second number to be added
* @returns `number` - the sum of the 2 parameters
*/
const sumOfTwoIntegers = (a, b) => new Array(a).fill(0).concat(new Array(b).fill(0)).length

sumOfTwoIntegers(5, 19)